n= input ('digite algo: ')
print(n.isnumeric()) #n é numerico?
print(n.isalpha()) #n é letra?
print(n.isalnum()) #n é alfa númerico?
print(n.isupper()) #está somente com letras maiúsculas?
print(n.isspace()) #só tem espaço?
print(n.islower()) #está em letras minúsculas
print(n.isdigit()) # é um método de string que verifica se todos os caracteres dentro dela são dígitos (0 a 9)
print(n.isascii()) #todos os caráteres possuem oq estão em ASCII sem acentos